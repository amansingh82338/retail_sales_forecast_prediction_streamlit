"""
pages/3_Model_Comparison.py
=============================
Page 3 of the Predictive Sales Forecasting dashboard.
Compares the 4 trained models on MAE / RMSE / R2, shows feature importance,
and displays the actual-vs-predicted plot for the best model.
"""

import os

import streamlit as st
import plotly.express as px

from utils import REPORTS_DIR, load_csv_report

st.set_page_config(page_title="Model Comparison", page_icon="🏆", layout="wide")

st.title("🏆 Model Comparison")
st.caption("Compare Linear Regression, Decision Tree, Random Forest, and XGBoost.")

comparison_df = load_csv_report(os.path.join(REPORTS_DIR, "model_comparison.csv"))
feature_importance_df = load_csv_report(os.path.join(REPORTS_DIR, "feature_importance.csv"))

# --------------------------------------------------------------------------
# EVALUATION METRICS
# --------------------------------------------------------------------------
if comparison_df is not None:
    best_row = comparison_df.sort_values("R2_Score", ascending=False).iloc[0]
    st.success(f"🏅 Best model: **{best_row['Model']}**  (R² = {best_row['R2_Score']:.4f})")

    m1, m2, m3 = st.columns(3)
    m1.metric("Best MAE", f"{comparison_df['MAE'].min():,.2f}")
    m2.metric("Best RMSE", f"{comparison_df['RMSE'].min():,.2f}")
    m3.metric("Best R²", f"{comparison_df['R2_Score'].max():.4f}")

    st.subheader("Evaluation Metrics Table")
    st.dataframe(
        comparison_df.sort_values("R2_Score", ascending=False).reset_index(drop=True),
        use_container_width=True,
    )

    st.subheader("Metric Comparison Charts")
    metric_cols = [c for c in ["MAE", "RMSE", "R2_Score"] if c in comparison_df.columns]
    chart_cols = st.columns(len(metric_cols)) if metric_cols else []

    for i, metric in enumerate(metric_cols):
        with chart_cols[i]:
            sorted_df = comparison_df.sort_values(metric, ascending=(metric != "R2_Score"))
            fig = px.bar(
                sorted_df, x="Model", y=metric, color="Model",
                title=metric.replace("_", " "),
            )
            fig.update_layout(showlegend=False)
            st.plotly_chart(fig, use_container_width=True)

    st.subheader("All Metrics Side-by-Side")
    melted = comparison_df.melt(id_vars="Model", value_vars=metric_cols,
                                 var_name="Metric", value_name="Value")
    fig = px.bar(melted, x="Model", y="Value", color="Metric", barmode="group",
                 title="MAE / RMSE / R² by Model")
    st.plotly_chart(fig, use_container_width=True)

else:
    st.warning(
        "No model_comparison.csv found in the reports folder. "
        "Run train_model.py first to generate evaluation reports."
    )

st.divider()

# --------------------------------------------------------------------------
# FEATURE IMPORTANCE
# --------------------------------------------------------------------------
st.subheader("Feature Importance")
if feature_importance_df is not None:
    top_n = st.slider("Number of top features to show", min_value=5, max_value=30, value=15)
    top_features = (
        feature_importance_df.sort_values("Importance", ascending=False)
        .head(top_n)
        .sort_values("Importance")
    )
    fig = px.bar(
        top_features, x="Importance", y="Feature", orientation="h",
        title=f"Top {top_n} Most Important Features",
    )
    fig.update_layout(height=max(400, top_n * 25))
    st.plotly_chart(fig, use_container_width=True)

    with st.expander("View full feature importance table"):
        st.dataframe(
            feature_importance_df.sort_values("Importance", ascending=False).reset_index(drop=True),
            use_container_width=True,
        )
else:
    st.info("Feature importance report not found. Run train_model.py to generate it.")

st.divider()

# --------------------------------------------------------------------------
# ACTUAL VS PREDICTED
# --------------------------------------------------------------------------
st.subheader("Actual vs Predicted (Best Model)")
avp_path = os.path.join(REPORTS_DIR, "actual_vs_predicted.png")
if os.path.exists(avp_path):
    st.image(avp_path, use_container_width=True)
else:
    st.info("actual_vs_predicted.png not found in the reports folder.")

# --------------------------------------------------------------------------
# MODEL COMPARISON IMAGE (pre-generated by train_model.py)
# --------------------------------------------------------------------------
st.subheader("Model Comparison Chart (from training report)")
comp_png_path = os.path.join(REPORTS_DIR, "model_comparison.png")
if os.path.exists(comp_png_path):
    st.image(comp_png_path, use_container_width=True)
else:
    st.info("model_comparison.png not found in the reports folder.")